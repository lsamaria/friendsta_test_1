//MARK:- UNUserNotificationCenter Methods
extension SceneDelegate: UNUserNotificationCenterDelegate {
    
    // 2. this runs second upon launch
    fileprivate func attemptRegistrationForNotifications() {
        
        UNUserNotificationCenter.current().delegate = self
        
        let authOptions: UNAuthorizationOptions = [.alert, .badge, .sound]
        
        UNUserNotificationCenter.current().requestAuthorization(options: authOptions) {
            [weak self](granted, error) in
            
            if let error = error {
                print(error.localizedDescription)
                return
            }
            
            guard granted else {
                print("\n2A.<-<-<>->->Notifications Denied")
                return
            }
            print("\n2.<-<-<>->->Notifications Granted")
            self?.getNotificationSettings()
            
        }
        UIApplication.shared.registerForRemoteNotifications() //application.registerForRemoteNotifications()
    }
    
    // 2A. If the user declines permissions
    func getNotificationSettings() {
        UNUserNotificationCenter.current().getNotificationSettings { settings in
            print("Notification settings: \(settings)")
            
            guard settings.authorizationStatus == .authorized else { return }
            DispatchQueue.main.async {
                UIApplication.shared.registerForRemoteNotifications()
            }
        }
    }
}
